namespace CDS.WinFormsMenus.Basic;

/// <summary>
/// Specifies which mouse interactions activate a menu item's action.
/// </summary>
public enum MouseActivation
{
    /// <summary>
    /// A single click activates the item.
    /// </summary>
    SingleClick,

    /// <summary>
    /// A double-click activates the item. This is the default behaviour.
    /// </summary>
    DoubleClick,
}
